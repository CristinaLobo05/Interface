package Interfaces;

public class act8 {
    public static void main(String[] args) {
        boolean bool1 = true;
        boolean bool2 = false;

        System.out.println("AND: " + (bool1 && bool2));
        System.out.println("OR: " + (bool1 || bool2));
        System.out.println("NOT bool1: " + (!bool1));
        System.out.println("NOT bool2: " + (!bool2));
    }
}
